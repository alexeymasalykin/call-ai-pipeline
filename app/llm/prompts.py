# Novofon stereo layout: channel 1 = called party, channel 2 = caller
_ROLE_MAPS = {
    "outgoing": {"Спикер 1": "Клиент", "Спикер 2": "Менеджер"},
    "incoming": {"Спикер 1": "Менеджер", "Спикер 2": "Клиент"},
}


def build_user_prompt(
    transcript: str, phone_number: str, duration: int, direction: str,
) -> str:
    """Build user prompt with role-mapped transcript and direction context."""
    role_map = _ROLE_MAPS.get(direction, _ROLE_MAPS["incoming"])

    transcript_with_roles = transcript
    for speaker, role in role_map.items():
        transcript_with_roles = transcript_with_roles.replace(
            f"{speaker}:", f"{role}:",
        )

    if direction == "outgoing":
        call_direction_label = "Исходящий (мы звоним клиенту)"
        caller_or_client_label = "клиента"
    else:
        call_direction_label = "Входящий (клиент звонит нам)"
        caller_or_client_label = "звонящего"

    return _USER_PROMPT_TEMPLATE.format(
        call_direction_label=call_direction_label,
        caller_or_client_label=caller_or_client_label,
        phone_number=phone_number,
        duration=duration,
        transcript_with_roles=transcript_with_roles,
        direction=direction,
    )
